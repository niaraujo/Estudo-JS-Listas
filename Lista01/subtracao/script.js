//Faça um programa que receba dois valores e faça a subtração 
let subNumero1 = Number(prompt("Informe um número para a subtração"));
let subNumero2 = Number(prompt("Informe outro número para a subtração"));
let resultado = subNumero1 - subNumero2;

alert(`a subtração de ${subNumero1} - ${subNumero2} é: ${resultado}`);