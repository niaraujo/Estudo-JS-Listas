//Para os números de 1 a 20, imprima: "Número X é menor que 10", "igual a 10" ou "maior que 10".
for(let i = 1; i<=20; i++){
    if(i<10){
        console.log(`${i} é menor que 10`)
    }else if(i == 10){
        console.log(`${i} é igual a 10`)
    }else{
        console.log(`${i} é maior que 10`)
    }
}